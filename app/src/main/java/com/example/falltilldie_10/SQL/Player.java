package com.example.falltilldie_10.SQL;

import android.content.ContentValues;
import android.database.sqlite.SQLiteDatabase;

public class Player {
    int id;
    String name;
    int point;


    public Player(int id, String name, int point) {
        this.id = id;
        this.name = name;
        this.point = point;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getPoint() {
        return point;
    }

    public void setPoint(int point) {
        this.point = point;
    }
}
